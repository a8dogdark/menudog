	STA KEMBUFFER,y
	STA KEMBUFFER+$100,y
	STA KEMBUFFER+$200,y
	STA KEMBUFFER+$300,y
	INY
	BNE KEMLOOP2
	DEC $D301
	LDX #$01
	LDY #$4C
	LDA #$13
	STX $EE17
	STY $ED8F
	STA $ED67
	LDX #$80
	LDY #$03
	STX $EBA3
	STY $EBA8
	LDY #$04
	LDA #$EA
NOP
