KEMBUFFER =$B000
ROMRAM
	LDY #$00
KEMLOOP1
	LDA $e000,y
	STA KEMBUFFER,y
	LDA $e100,y
	STA KEMBUFFER+$100,y
	LDA $e200,y
	STA KEMBUFFER+$200,y
	LDA $e300,y
	STA KEMBUFFER+$300,y
	INY
	BNE KEMLOOP1
	LDA #>KEMBUFFER
	STA CHBAS
	STA CHBASE
	LDX #$C0
	SEI
	STY $D40E
	STX $CC
	STY $CB
LOOP
	LDA ($CB),Y
	DEC $D301
	STA ($CB),Y
	INC $D301
	INY
	BNE LOOP
	INC $CC
	BEQ @EXIT
	LDA $CC
	CMP #$D0
	BNE LOOP
	LDA #$D8
	STA $CC
	BNE LOOP
@EXIT
	LDA #$E0
	STA CHBAS
	STA CHBASE
	TYA	//y está en cero, según el código de arriba
