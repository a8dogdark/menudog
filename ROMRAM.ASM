	DEY
	BPL NOP
	LDY #STACF-STACI
MOVE
	LDA STACI,Y
	STA $ECEF,Y
	DEY
	BPL MOVE
	LDA #$40
	STA NMIEN
	CLI
	CLC
	RTS
